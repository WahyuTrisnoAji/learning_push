package javafol;

public class LatihanUas12 {
    public static void main(String[] args) {
        int day = 2;
        switch (day) {
            case 1:
                System.out.println("minggu");
                break;
            case 2:
                System.out.println("senin");
                break;
            case 3:
                System.out.println("selasa");
                break;
            case 4:
                System.out.println("rabu");
                break;
            default:
                System.out.print("hari lainnya");
        }
    }
}
